
DROP TABLE ROUTE_STOPS;
DROP TABLE BUS_ROUTES;
DROP TABLE BUS_STOPS;
DROP TABLE TRANSPORT_STATS;


CREATE TABLE BUS_ROUTES (
    route_id VARCHAR2(100) PRIMARY KEY, 
    route_short_name VARCHAR2(100),
    route_long_name VARCHAR2(500),
    route_type VARCHAR2(50)             
);


CREATE TABLE BUS_STOPS (
    stop_id NUMBER PRIMARY KEY,
    stop_name VARCHAR2(255),
    stop_lat NUMBER,
    stop_lon NUMBER
);



CREATE TABLE ROUTE_STOPS (
    route_id NUMBER,
    stop_id NUMBER,
    stop_sequence NUMBER,
    CONSTRAINT pk_route_stop PRIMARY KEY (route_id, stop_id),
    CONSTRAINT fk_route FOREIGN KEY (route_id) REFERENCES BUS_ROUTES(route_id),
    CONSTRAINT fk_stop FOREIGN KEY (stop_id) REFERENCES BUS_STOPS(stop_id)
);


CREATE TABLE TRANSPORT_STATS (
    stat_id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    report_date DATE,
    ridership NUMBER,
    gas_price NUMBER,
    on_time_performance NUMBER
);
